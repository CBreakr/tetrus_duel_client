export default class SmileyFaceGrid extends React.Component {

    render() {

        let grid = [];

        if(this.props.is_winner){
            grid = winner_grid;
        }
        else{
            grid = loser_grid;
        }

        return (
            <table className="game-board smiley">
                <tbody>
                    {
                        grid.map((row, r_index) => {
                            return <tr key={r_index}>
                            {
                                row.map((cell, c_index) => {
                                    const cell_value = row[c_index];
                                    let classesForCell = "";
                                    if(cell_value === 0){
                                        classesForCell = "empty";
                                    }
                                    else if(cell_value === 1){
                                        classesForCell = "filled";
                                    }
                                    return <td className={`game-cell ${classesForCell}`} key={`c_${c_index}`}></td>
                                })
                            }
                            </tr>
                        })
                    }
                </tbody>
            </table>
        )
    }
}